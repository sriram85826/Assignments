package Zoho1;

public class TypeConversions {

	public static void main(String[] args) {
				int a=(int)2.5f;
				System.out.println("Float to int:"+a);
				float b=(float)2.5;
				System.out.println("Double to float:"+b);
				short n=(short)32768;
				System.out.println((short) n);
	}
}
